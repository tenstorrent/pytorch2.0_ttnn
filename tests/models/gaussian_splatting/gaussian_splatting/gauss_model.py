import torch
import torch.nn as nn
import numpy as np
from gaussian_splatting.utils.point_utils import PointCloud
from gaussian_splatting.gauss_render import strip_symmetric, inverse_sigmoid, build_scaling_rotation
from gaussian_splatting.utils.sh_utils import RGB2SH


def compute_knn_distances_cpu(points_tensor, k=3):  # Added to avoid CUDA dependency
    """
    Compute k-nearest neighbor distances using pure PyTorch (CPU)
    Args:
        points_tensor: [N, 3] tensor of points
        k: number of nearest neighbors
    Returns:
        [N] tensor of distances to k-th nearest neighbor
    """
    # Compute pairwise distances
    points_tensor = points_tensor.cpu()  # Ensure CPU
    distances = torch.cdist(points_tensor, points_tensor)  # [N, N]

    # Get k+1 nearest (including self at distance 0)
    knn_distances, _ = torch.topk(distances, k + 1, dim=1, largest=False)

    # Return distance to k-th nearest neighbor (excluding self)
    return knn_distances[:, k]  # or knn_distances[:, 1] for 1st nearest


class GaussModel(nn.Module):
    """
    A Gaussian Model

    * Attributes
    _xyz: locations of gaussians
    _feature_dc: DC term of features
    _feature_rest: rest features
    _rotatoin: rotation of gaussians
    _scaling: scaling of gaussians
    _opacity: opacity of gaussians

    >>> gaussModel = GaussModel.create_from_pcd(pts)
    >>> gaussRender = GaussRenderer()
    >>> out = gaussRender(pc=gaussModel, camera=camera)
    """

    def setup_functions(self):
        def build_covariance_from_scaling_rotation(scaling, scaling_modifier, rotation):
            L = build_scaling_rotation(scaling_modifier * scaling, rotation)
            actual_covariance = L @ L.transpose(1, 2)
            symm = strip_symmetric(actual_covariance)
            return symm

        self.scaling_activation = torch.exp
        self.scaling_inverse_activation = torch.log

        self.covariance_activation = build_covariance_from_scaling_rotation

        self.opacity_activation = torch.sigmoid
        self.inverse_opacity_activation = inverse_sigmoid

        self.rotation_activation = torch.nn.functional.normalize

    def __init__(self, sh_degree: int = 3, debug=False):
        super(GaussModel, self).__init__()
        self.max_sh_degree = sh_degree
        self._xyz = torch.empty(0)
        self._features_dc = torch.empty(0)
        self._features_rest = torch.empty(0)
        self._scaling = torch.empty(0)
        self._rotation = torch.empty(0)
        self._opacity = torch.empty(0)
        self.setup_functions()
        self.debug = debug

    def create_from_pcd(self, pcd: PointCloud):
        """
        create the guassian model from a color point cloud
        """
        points = pcd.coords
        colors = pcd.select_channels(["R", "G", "B"]) / 255.0

        fused_point_cloud = torch.tensor(np.asarray(points))
        fused_color = RGB2SH(torch.tensor(np.asarray(colors)))

        print("Number of points at initialisation : ", fused_point_cloud.shape[0])

        features = torch.zeros((fused_color.shape[0], 3, (self.max_sh_degree + 1) ** 2))
        features[:, :3, 0] = fused_color
        features[:, 3:, 1:] = 0.0

        points_tensor = torch.from_numpy(np.asarray(points)).float()  # Added to avoid CUDA dependency
        dist2 = torch.clamp_min(compute_knn_distances_cpu(points_tensor, k=1), 0.0000001)
        scales = torch.log(torch.sqrt(dist2))[..., None].repeat(1, 3)
        rots = torch.zeros((fused_point_cloud.shape[0], 4))
        rots[:, 0] = 1
        opacities = inverse_sigmoid(0.1 * torch.ones((fused_point_cloud.shape[0], 1)))

        if self.debug:
            # easy for visualization
            colors = np.zeros_like(colors)
            opacities = inverse_sigmoid(0.9 * torch.ones((fused_point_cloud.shape[0], 1)))

        self._xyz = nn.Parameter(fused_point_cloud.requires_grad_(True))
        self._features_dc = nn.Parameter(features[:, :, 0:1].transpose(1, 2).contiguous().requires_grad_(True))
        self._features_rest = nn.Parameter(features[:, :, 1:].transpose(1, 2).contiguous().requires_grad_(True))
        self._scaling = nn.Parameter(scales.requires_grad_(True))
        self._rotation = nn.Parameter(rots.requires_grad_(True))
        self._opacity = nn.Parameter(opacities.requires_grad_(True))
        self.max_radii2D = torch.zeros((self._xyz.shape[0]))
        return self

    @property
    def get_scaling(self):
        return self.scaling_activation(self._scaling)

    @property
    def get_rotation(self):
        return self.rotation_activation(self._rotation)

    @property
    def get_xyz(self):
        return self._xyz

    @property
    def get_features(self):
        features_dc = self._features_dc
        features_rest = self._features_rest
        return torch.cat((features_dc, features_rest), dim=1)

    @property
    def get_opacity(self):
        return self.opacity_activation(self._opacity)

    def get_covariance(self, scaling_modifier=1):
        return self.covariance_activation(self.get_scaling, scaling_modifier, self._rotation)

    def save_ply(self, path):
        from plyfile import PlyData, PlyElement

        xyz = self._xyz.detach().cpu().numpy()
        normals = np.zeros_like(xyz)
        f_dc = self._features_dc.detach().transpose(1, 2).flatten(start_dim=1).contiguous().cpu().numpy()
        f_rest = self._features_rest.detach().transpose(1, 2).flatten(start_dim=1).contiguous().cpu().numpy()
        opacities = self._opacity.detach().cpu().numpy()
        scale = self._scaling.detach().cpu().numpy()
        rotation = self._rotation.detach().cpu().numpy()

        dtype_full = [(attribute, "f4") for attribute in self.construct_list_of_attributes()]

        elements = np.empty(xyz.shape[0], dtype=dtype_full)
        attributes = np.concatenate((xyz, normals, f_dc, f_rest, opacities, scale, rotation), axis=1)
        elements[:] = list(map(tuple, attributes))
        el = PlyElement.describe(elements, "vertex")
        PlyData([el]).write(path)

    def construct_list_of_attributes(self):
        l = ["x", "y", "z", "nx", "ny", "nz"]
        # All channels except the 3 DC
        for i in range(self._features_dc.shape[1] * self._features_dc.shape[2]):
            l.append("f_dc_{}".format(i))
        for i in range(self._features_rest.shape[1] * self._features_rest.shape[2]):
            l.append("f_rest_{}".format(i))
        l.append("opacity")
        for i in range(self._scaling.shape[1]):
            l.append("scale_{}".format(i))
        for i in range(self._rotation.shape[1]):
            l.append("rot_{}".format(i))
        return l
